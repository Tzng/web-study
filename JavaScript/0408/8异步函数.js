/**
 * 使用for循环从1到5遍历i，每次循环都会调用一个匿名函数，这个匿名函数会在j秒后输出j的值。
 * 为了解决setTimeout函数会在循环结束后才执行的问题，我们使用了闭包来保存每个i的值。
 */
/**
 * 闭包是指有权访问另一个函数作用域中的变量的函数，创建闭包的最常见的方式就是在一个函数内部创建另一个函数，通过另一个函数访问这个函数的局部变量。
 * 在 JavaScript 中，只有函数内部的子函数才能读取局部变量，因此可以把闭包简单理解成“定义在一个函数内部的函数”。闭包可以用来模拟私有方法等。
 */
for (var i = 1; i <= 5; i++) {
    (function (j) {
        setTimeout( function timer() {
            console.log(j);
        }, j * 1000 );
    })(i);
}